/*
  Crie uma função chamada somar() que recebe dois parâmetros de entrada chamados 'a' e 'b' e em seguida retorne a soma entre a e b (não altere o restante do arquivo)
*/

/**** Escreva o código abaixo 👇******/

function somar(a, b) {
  return a + b;
};

